                сопроводительного листа, в котором указывается наименование товара и его предполагаемая неисправность.
                Сопроводительный лист заполняется и подписывается Покупателем. На товар, принятый на гарантийный ремонт,
                Покупателю выдаётся опись, заполненная Продавцом;
