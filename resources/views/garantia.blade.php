                (станки, металлорежущие и деревообрабатывающие, бытовые, электробытовые машины и приборы, бытовая
                радиоэлектронная аппаратура, бытовая вычислительная и множительная техника, фото и киноаппаратура,
                телефонные аппараты и факсимильная аппаратура, электромузыкальные инструменты, игрушки электронные,
